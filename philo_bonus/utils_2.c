#include "philo_bonus.h"

size_t	ft_strlen(const char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

void	error(char *msg)
{
	int	i;

	i = ft_strlen(msg);
	write(1, "Error: ", 7);
	write(1, msg, i);
	write(1, "\n", 1);
	exit (1);
}

unsigned long long	get_time(void)
{
	struct timeval	current_time;

	gettimeofday(&current_time, NULL);
	return (current_time.tv_sec * 1000 + current_time.tv_usec / 1000);
}
